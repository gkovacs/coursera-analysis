def timeToSeconds(timestr):
  minutes,seconds = tuple(timestr.split(':'))
  minutes = int(minutes)
  seconds = int(seconds)
  return minutes*60 + seconds

class TRange:
  def __init__(self, start, end):
    if type(start) == type(''):
      start = timeToSeconds(start)
    if type(end) == type(''):
      end = timeToSeconds(end)
    self.start = start
    self.end = end

